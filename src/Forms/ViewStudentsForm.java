    private void StudentInfoTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_StudentInfoTableMousePressed
        if (evt.getClickCount() == 2) {
            int r = StudentInfoTable.getSelectedRow();
            String usn = (String) StudentInfoTable.getValueAt(r, 0);
            String name = (String) StudentInfoTable.getValueAt(r, 1);
            String gender = (String) StudentInfoTable.getValueAt(r, 2);
            String fname = (String) StudentInfoTable.getValueAt(r, 3);
            String dob = (String) StudentInfoTable.getValueAt(r, 4);
            String branch = (String) StudentInfoTable.getValueAt(r, 5);
            String sslc = (String) StudentInfoTable.getValueAt(r, 6);
            String puc = (String) StudentInfoTable.getValueAt(r, 7);
            String be = (String) StudentInfoTable.getValueAt(r, 8);
            String phone = (String) StudentInfoTable.getValueAt(r, 9);
            String email = (String) StudentInfoTable.getValueAt(r, 10);
            String yop = (String) StudentInfoTable.getValueAt(r, 11);

            AddStudentForm ASFObj = new AddStudentForm();
            ASFObj.update(usn, name, gender, fname, dob, branch, sslc, puc, be, phone, email, yop);
            ASFObj.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_StudentInfoTableMousePressed

