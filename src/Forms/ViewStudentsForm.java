    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String searchName = SearchText.getText();
        ResultSet rs = VSObj.getData(searchName);

        try {
            if (!rs.next()) {
                JOptionPane.showMessageDialog(null, "NO RECORDS FOUND", "ERROR", JOptionPane.INFORMATION_MESSAGE);
            } else {
                rs.previous();
                StudentInfoTable.setModel(DbUtils.resultSetToTableModel(rs));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ViewStudentsForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_searchBtnActionPerformed

